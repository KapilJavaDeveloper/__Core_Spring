package org.example;

public class Point {
    private int X  ;
    private int Y ;

    // setter
    public void setX(int X){
        this.X = X ;
    }
    public void setY(int Y){
        this.Y = Y ;
    }

    // getter
    public int getX(){
        return this.X ;
    }
    public int getY(){
        return this.Y ;
    }


}
